// Inheritance is inheriting one class to another class so we can use,
// its content in other class.

// subclass (child) - the class that inherits from another class
// superclass (parent) - the class being inherited from

// to inherit a class use extends keyword.
// Syntax subclass extends superclass
public class _30Inheritance {
    protected String name = "Ford";
    public void Move() {
        System.out.println("This car moves forward");
    }
    
}


