public class _11Switch {
    public static void main(String[] args) {
        
        // Switch statment is a coditon checking block;
        // If there are more codition to check then place it in switch
        // Eg.
        
        int day = 5;
        switch (day) {
            case 1:
                System.out.println("Hi");
                break;
            case 2:
                System.out.println("Hello");    
                break;
            case 3:
                System.out.println("Hey");
                break;
            case 4:
                System.out.println("Hola");
                break;
            case 5:
                System.out.println("Bye");
                break;            
            default:
                break;
        }
    }
}
