public class _8MathClass {

    public static void main(String[] args) {
        
        // Math Class is used to do all math work in program.
        // Math class contain functions that help to do math work.

        // Eg. of math function as follow:

        // Max function return maximum number of two numbers as Follow:
        System.out.println(Math.max(10, 50));

        // min function return minimum number of two numbers as Follow:
        System.out.println(Math.min(10,30));

        // sqrt function return the square root of the number as Follow:
        System.out.println((int)Math.sqrt(64));

        // abs function returns the absolute positive number as follow:
        System.out.println(Math.abs(-21));

        // random function returns the random number.
        System.out.println((int)Math.random());
        
        // To produce random numer between givn range do as follow:
        System.out.println((int)(Math.random()*6));
        // This will produce randome number between 0-5
        
        System.out.println(1+(int)(Math.random()*11));
        // This will produce random number between 1-10
    }
    
}
